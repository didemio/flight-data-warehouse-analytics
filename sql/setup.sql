-- 3.2.1 Fuel-cost intensity by aircraft model and route type
SELECT
    da.model_name,
    dr.route_type,
    ROUND(
        AVG(
            fn_estimated_fuel_cost(ff.distance_miles,
                                   ff.seat_capacity,
                                   da.model_name)
            / NULLIF(ff.distance_miles,0)
        ),
        2
    ) AS avg_fuel_cost_per_mile
FROM fact_flight_metrics ff
     JOIN dim_route    dr ON ff.route_key    = dr.route_key
     JOIN dim_aircraft da ON ff.aircraft_key = da.aircraft_key
GROUP BY da.model_name, dr.route_type
ORDER BY da.model_name, dr.route_type;

