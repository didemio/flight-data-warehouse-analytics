SELECT
    year_num,
    route_name,
    scenario,
    ROUND(total_revenue,2)    AS total_revenue,
    ROUND(total_fuel_cost,2)  AS total_fuel_cost,
    ROUND(total_profit,2)     AS total_profit
FROM (
    SELECT
        dd.year_num,
        dr.route_name,
        CAST('BASE' AS VARCHAR2(20)) AS scenario,   -- <<< KEY FIX
        SUM(ff.total_ticket_revenue) AS total_revenue,
        SUM(ff.estimated_fuel_cost)  AS total_fuel_cost,
        SUM(ff.total_profit)         AS total_profit
    FROM fact_flight_metrics ff
         JOIN dim_date  dd ON ff.date_key  = dd.date_key
         JOIN dim_route dr ON ff.route_key = dr.route_key
    GROUP BY dd.year_num, dr.route_name
)
MODEL
    PARTITION BY (year_num, route_name)
    DIMENSION BY (scenario)
    MEASURES (total_revenue, total_fuel_cost, total_profit)
    RULES UPSERT (
        total_revenue['FUEL+15']   = total_revenue['BASE'],
        total_fuel_cost['FUEL+15'] = total_fuel_cost['BASE'] * 1.15,
        total_profit['FUEL+15']    = total_revenue['FUEL+15']
                                     - total_fuel_cost['FUEL+15']
    )
ORDER BY year_num, route_name, scenario;

