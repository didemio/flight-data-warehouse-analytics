-- 3.1.1 ROLLUP: revenue, profit, load factor by year, quarter, route
SELECT
    dd.year_num,
    dd.quarter_num,
    dr.route_name,
    SUM(ff.total_ticket_revenue) AS total_revenue,
    SUM(ff.total_profit)         AS total_profit,
    ROUND(AVG(ff.load_factor_pct),2) AS avg_load_factor,
    GROUPING(dd.year_num)       AS grp_year,
    GROUPING(dd.quarter_num)    AS grp_quarter,
    GROUPING(dr.route_name)     AS grp_route
FROM fact_flight_metrics ff
     JOIN dim_date  dd ON ff.date_key  = dd.date_key
     JOIN dim_route dr ON ff.route_key = dr.route_key
GROUP BY ROLLUP (dd.year_num, dd.quarter_num, dr.route_name)
ORDER BY dd.year_num, dd.quarter_num, dr.route_name;

-- 3.1.2 CUBE: avg arrival delay & on-time rate by origin country and aircraft model
SELECT
    dr.origin_country,
    da.model_name,
    ROUND(AVG(ff.arrival_delay_min),1)          AS avg_arrival_delay_min,
    ROUND(AVG(ff.on_time_indicator)*100,2)      AS on_time_pct
FROM fact_flight_metrics ff
     JOIN dim_route    dr ON ff.route_key    = dr.route_key
     JOIN dim_aircraft da ON ff.aircraft_key = da.aircraft_key
GROUP BY CUBE (dr.origin_country, da.model_name)
ORDER BY dr.origin_country, da.model_name;

-- 3.1.3 GROUPING SETS: monthly revenue and load factor by route & route type
SELECT
    dd.year_num,
    dd.month_num,
    dr.route_type,
    dr.route_name,
    SUM(ff.total_ticket_revenue)      AS total_revenue,
    ROUND(AVG(ff.load_factor_pct),2)  AS avg_load_factor
FROM fact_flight_metrics ff
     JOIN dim_date  dd ON ff.date_key  = dd.date_key
     JOIN dim_route dr ON ff.route_key = dr.route_key
GROUP BY GROUPING SETS (
    (dd.year_num, dd.month_num, dr.route_type, dr.route_name),
    (dd.year_num, dd.month_num, dr.route_type),
    (dd.year_num, dd.month_num),
    ()
)
ORDER BY dd.year_num, dd.month_num, dr.route_type, dr.route_name;

-- 3.1.4 ROLLUP with PL/SQL function: flights by delay category and year
SELECT
    dd.year_num,
    fn_delay_category(ff.arrival_delay_min) AS delay_category,
    COUNT(*)                                AS flight_count,
    ROUND(AVG(ff.arrival_delay_min),1)      AS avg_arrival_delay,
    GROUPING(dd.year_num)                   AS grp_year,
    GROUPING(fn_delay_category(ff.arrival_delay_min)) AS grp_cat
FROM fact_flight_metrics ff
     JOIN dim_date dd ON ff.date_key = dd.date_key
GROUP BY ROLLUP (dd.year_num, fn_delay_category(ff.arrival_delay_min))
ORDER BY dd.year_num, delay_category;

