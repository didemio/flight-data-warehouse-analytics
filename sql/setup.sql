CREATE OR REPLACE PROCEDURE pr_load_flight_dw AS
BEGIN
    ------------------------------------------------------------
    -- DIM_DATE: full year 2025
    ------------------------------------------------------------
    EXECUTE IMMEDIATE 'TRUNCATE TABLE dim_date';

    INSERT INTO dim_date (
        date_key, full_date, day_of_week, day_of_month,
        iso_week_of_year, month_num, month_name,
        quarter_num, year_num, is_weekend
    )
    SELECT
        TO_NUMBER(TO_CHAR(d, 'YYYYMMDD')) AS date_key,
        d                                  AS full_date,
        RTRIM(TO_CHAR(d, 'DAY'))          AS day_of_week,
        TO_NUMBER(TO_CHAR(d, 'DD'))       AS day_of_month,
        TO_NUMBER(TO_CHAR(d, 'IW'))       AS iso_week_of_year,
        TO_NUMBER(TO_CHAR(d, 'MM'))       AS month_num,
        TO_CHAR(d, 'MON')                 AS month_name,
        TO_NUMBER(TO_CHAR(d, 'Q'))        AS quarter_num,
        TO_NUMBER(TO_CHAR(d, 'YYYY'))     AS year_num,
        CASE
            WHEN TO_CHAR(d, 'D') IN ('1','7') THEN 'Y'
            ELSE 'N'
        END AS is_weekend
    FROM (
        SELECT DATE '2025-01-01' + LEVEL - 1 AS d
        FROM dual
        CONNECT BY LEVEL <= 365
    );

    ------------------------------------------------------------
    -- DIM_ROUTE
    ------------------------------------------------------------
    EXECUTE IMMEDIATE 'TRUNCATE TABLE dim_route';

    INSERT INTO dim_route (
        route_id, route_name,
        origin_airport_code, origin_airport_name, origin_city, origin_country,
        dest_airport_code, dest_airport_name, dest_city, dest_country,
        distance_miles, route_type
    )
    SELECT
        r.route_id,
        r.origin_airport || '-' || r.destination_airport AS route_name,
        ao.airport_code,
        ao.airport_name,
        ao.city,
        ao.country,
        ad.airport_code,
        ad.airport_name,
        ad.city,
        ad.country,
        r.distance_miles,
        CASE
            WHEN ao.country = ad.country THEN 'Domestic'
            ELSE 'International'
        END AS route_type
    FROM stg_routes r
         JOIN stg_airports ao ON r.origin_airport      = ao.airport_code
         JOIN stg_airports ad ON r.destination_airport = ad.airport_code;

    ------------------------------------------------------------
    -- DIM_AIRCRAFT
    ------------------------------------------------------------
    EXECUTE IMMEDIATE 'TRUNCATE TABLE dim_aircraft';

    INSERT INTO dim_aircraft (
        tail_number, model_id, model_name,
        seat_capacity, acquisition_date,
        effective_start_date, effective_end_date, is_current
    )
    SELECT
        fa.tail_number,
        am.model_id,
        am.model_name,
        am.seat_capacity,
        fa.acquisition_date,
        fa.acquisition_date,
        NULL,
        'Y'
    FROM stg_fleet_aircraft fa
         JOIN stg_aircraft_models am ON fa.model_id = am.model_id;

    ------------------------------------------------------------
    -- DIM_TIME_OF_DAY
    ------------------------------------------------------------
    EXECUTE IMMEDIATE 'TRUNCATE TABLE dim_time_of_day';

    INSERT INTO dim_time_of_day (time_key, hour_of_day, time_period)
    SELECT
        h AS time_key,
        h AS hour_of_day,
        CASE
            WHEN h BETWEEN 5 AND 10  THEN 'Morning'
            WHEN h BETWEEN 11 AND 16 THEN 'Afternoon'
            WHEN h BETWEEN 17 AND 21 THEN 'Evening'
            ELSE 'Night'
        END AS time_period
    FROM (
        SELECT LEVEL-1 AS h
        FROM dual
        CONNECT BY LEVEL <= 24
    );

    ------------------------------------------------------------
    -- FACT_FLIGHT_METRICS
    ------------------------------------------------------------
    EXECUTE IMMEDIATE 'TRUNCATE TABLE fact_flight_metrics';

    INSERT INTO fact_flight_metrics (
        flight_id,
        date_key, route_key, aircraft_key, dep_time_key,
        tickets_sold, seat_capacity, load_factor_pct,
        total_ticket_revenue, estimated_fuel_cost, total_profit,
        departure_delay_min, arrival_delay_min, on_time_indicator,
        distance_miles
    )
    WITH tkt AS (
        SELECT
            flight_id,
            COUNT(*)   AS tickets_sold,
            SUM(price) AS total_revenue
        FROM stg_tickets
        GROUP BY flight_id
    )
    SELECT
        f.flight_id,
        dd.date_key,
        dr.route_key,
        da.aircraft_key,
        td.time_key,
        NVL(t.tickets_sold,0) AS tickets_sold,
        da.seat_capacity,
        ROUND(NVL(t.tickets_sold,0) / da.seat_capacity * 100, 2) AS load_factor_pct,
        NVL(t.total_revenue,0) AS total_ticket_revenue,
        fn_estimated_fuel_cost(dr.distance_miles, da.seat_capacity, da.model_name)
            AS estimated_fuel_cost,
        NVL(t.total_revenue,0)
          - fn_estimated_fuel_cost(dr.distance_miles, da.seat_capacity, da.model_name)
            AS total_profit,
        ROUND( (f.actual_departure - f.scheduled_departure) * 24 * 60, 1 )
            AS departure_delay_min,
        ROUND( (f.actual_arrival   - f.scheduled_arrival)   * 24 * 60, 1 )
            AS arrival_delay_min,
        CASE
            WHEN (f.actual_arrival - f.scheduled_arrival) * 24 * 60 <= 15
            THEN 1 ELSE 0
        END AS on_time_indicator,
        dr.distance_miles
    FROM stg_flights f
         JOIN dim_route    dr ON dr.route_id    = f.route_id
         JOIN dim_aircraft da ON da.tail_number = f.tail_number
                               AND da.is_current = 'Y'
         LEFT JOIN tkt     t  ON t.flight_id    = f.flight_id
         JOIN dim_date     dd ON dd.full_date   = TRUNC(f.scheduled_departure)
         JOIN dim_time_of_day td
             ON td.hour_of_day = TO_NUMBER(TO_CHAR(f.scheduled_departure,'HH24'));

    COMMIT;
END;
/
SHOW ERRORS;

