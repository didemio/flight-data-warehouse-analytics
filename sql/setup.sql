CREATE TABLE dim_date (
    date_key          NUMBER(8)   CONSTRAINT pk_dim_date PRIMARY KEY, -- YYYYMMDD
    full_date         DATE        NOT NULL,
    day_of_week       VARCHAR2(10) NOT NULL,
    day_of_month      NUMBER(2)   NOT NULL,
    iso_week_of_year  NUMBER(2)   NOT NULL,
    month_num         NUMBER(2)   NOT NULL,
    month_name        VARCHAR2(10) NOT NULL,
    quarter_num       NUMBER(1)   NOT NULL,
    year_num          NUMBER(4)   NOT NULL,
    is_weekend        CHAR(1)     CHECK (is_weekend IN ('Y','N'))
);

CREATE TABLE dim_route (
    route_key              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    route_id               NUMBER        NOT NULL,
    route_name             VARCHAR2(20)  NOT NULL,
    origin_airport_code    VARCHAR2(3)   NOT NULL,
    origin_airport_name    VARCHAR2(100),
    origin_city            VARCHAR2(50),
    origin_country         VARCHAR2(50),
    dest_airport_code      VARCHAR2(3)   NOT NULL,
    dest_airport_name      VARCHAR2(100),
    dest_city              VARCHAR2(50),
    dest_country           VARCHAR2(50),
    distance_miles         NUMBER(6),
    route_type             VARCHAR2(20), -- Domestic / International
    CONSTRAINT pk_dim_route PRIMARY KEY (route_key),
    CONSTRAINT uk_dim_route_id UNIQUE (route_id)
);

CREATE TABLE dim_aircraft (
    aircraft_key         NUMBER GENERATED BY DEFAULT AS IDENTITY,
    tail_number          VARCHAR2(10) NOT NULL,
    model_id             NUMBER       NOT NULL,
    model_name           VARCHAR2(50) NOT NULL,
    seat_capacity        NUMBER(4)    NOT NULL,
    acquisition_date     DATE,
    effective_start_date DATE         NOT NULL,
    effective_end_date   DATE,
    is_current           CHAR(1)      DEFAULT 'Y'
                                   CHECK (is_current IN ('Y','N')),
    CONSTRAINT pk_dim_aircraft PRIMARY KEY (aircraft_key),
    CONSTRAINT uk_dim_aircraft UNIQUE (tail_number, effective_start_date)
);

CREATE TABLE dim_time_of_day (
    time_key    NUMBER(2)   CONSTRAINT pk_dim_time PRIMARY KEY,
    hour_of_day NUMBER(2)   NOT NULL CHECK (hour_of_day BETWEEN 0 AND 23),
