CREATE OR REPLACE FUNCTION fn_estimated_fuel_cost (
    p_distance_miles IN NUMBER,
    p_seat_capacity  IN NUMBER,
    p_model_name     IN VARCHAR2
) RETURN NUMBER IS
    v_base_rate_per_mile NUMBER;
    v_capacity_factor     NUMBER;
BEGIN
    IF p_distance_miles IS NULL OR p_distance_miles <= 0 THEN
        RETURN 0;
    END IF;

    IF p_model_name LIKE 'ATR%' THEN
        v_base_rate_per_mile := 3.0;
    ELSIF p_model_name LIKE 'Airbus A220%' THEN
        v_base_rate_per_mile := 4.0;
    ELSIF p_model_name LIKE 'Airbus A320%' THEN
        v_base_rate_per_mile := 4.5;
    ELSIF p_model_name LIKE 'Boeing 737%' THEN
        v_base_rate_per_mile := 4.7;
    ELSE
        v_base_rate_per_mile := 5.0;
    END IF;

    v_capacity_factor := 1 + NVL(p_seat_capacity,150) / 600;

    RETURN ROUND(p_distance_miles * v_base_rate_per_mile * v_capacity_factor, 2);
END;
/
SHOW ERRORS;

CREATE OR REPLACE FUNCTION fn_delay_category (
    p_arrival_delay_min IN NUMBER
) RETURN VARCHAR2 IS
BEGIN
    IF p_arrival_delay_min IS NULL THEN
        RETURN 'UNKNOWN';
    ELSIF p_arrival_delay_min <= -10 THEN
        RETURN 'EARLY';
    ELSIF p_arrival_delay_min <= 15 THEN
        RETURN 'ON_TIME';
    ELSIF p_arrival_delay_min <= 60 THEN
        RETURN 'LATE';
    ELSE
        RETURN 'VERY_LATE';
    END IF;
END;
/
SHOW ERRORS;

