DECLARE
    v_base_date DATE := DATE '2025-01-01';
    v_sched_dep DATE;
    v_sched_arr DATE;
    v_route_id  NUMBER;
    v_tail      VARCHAR2(10);
    v_block_hrs NUMBER;
BEGIN
    FOR i IN 1..60 LOOP
        v_route_id := CASE MOD(i,6)
                          WHEN 1 THEN 1
                          WHEN 2 THEN 2
                          WHEN 3 THEN 3
                          WHEN 4 THEN 4
                          WHEN 5 THEN 5
                          ELSE 6
                      END;

        v_tail := CASE MOD(i,6)
                      WHEN 1 THEN 'YL-ABC'
                      WHEN 2 THEN 'YL-DEF'
                      WHEN 3 THEN 'YL-GHI'
                      WHEN 4 THEN 'YL-JKL'
                      WHEN 5 THEN 'YL-MNO'
                      ELSE        'YL-PQR'
                  END;

        -- Schedule at 06, 12, 18, 22 each day
        v_sched_dep := v_base_date + TRUNC((i-1)/4)
                       + (CASE MOD(i,4)
                              WHEN 0 THEN 6
                              WHEN 1 THEN 12
                              WHEN 2 THEN 18
                              ELSE 22
                          END)/24;

        v_block_hrs := CASE v_route_id
                           WHEN 1 THEN 0.8
                           WHEN 2 THEN 0.8
                           WHEN 3 THEN 2.0
                           WHEN 4 THEN 2.3
                           WHEN 5 THEN 2.0
                           ELSE 5.5
                       END;

        v_sched_arr := v_sched_dep + v_block_hrs/24;

        INSERT INTO stg_flights (
            flight_id, route_id, tail_number,
            scheduled_departure, scheduled_arrival,
            actual_departure, actual_arrival
        )
        VALUES (
            i,
            v_route_id,
            v_tail,
            v_sched_dep,
            v_sched_arr,
            v_sched_dep + (DBMS_RANDOM.VALUE(-0.15, 0.35))/24,
            v_sched_arr + (DBMS_RANDOM.VALUE(-0.20, 0.60))/24
        );
    END LOOP;
    COMMIT;
END;
