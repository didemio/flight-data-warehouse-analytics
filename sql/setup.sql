-- 4.1 Aggregate table for monthly route performance
CREATE TABLE agg_monthly_route_perf (
    year_num              NUMBER(4)    NOT NULL,
    month_num             NUMBER(2)    NOT NULL,
    route_key             NUMBER       NOT NULL,
    route_name            VARCHAR2(20) NOT NULL,

    total_flights         NUMBER,
    total_tickets_sold    NUMBER,
    total_revenue         NUMBER(14,2),
    total_est_fuel_cost   NUMBER(14,2),
    total_profit          NUMBER(14,2),
    avg_load_factor_pct   NUMBER(5,2),
    avg_arrival_delay_min NUMBER(6,2),
    on_time_pct           NUMBER(5,2),

    CONSTRAINT pk_agg_monthly_route PRIMARY KEY (year_num, month_num, route_key)
);

-- 4.2 Population procedure (TRUNCATE + INSERT ... SELECT ... GROUP BY)
CREATE OR REPLACE PROCEDURE pr_populate_agg_monthly_route AS
BEGIN
    EXECUTE IMMEDIATE 'TRUNCATE TABLE agg_monthly_route_perf';

    INSERT INTO agg_monthly_route_perf (
        year_num, month_num, route_key, route_name,
        total_flights, total_tickets_sold,
        total_revenue, total_est_fuel_cost, total_profit,
        avg_load_factor_pct, avg_arrival_delay_min, on_time_pct
    )
    SELECT
        dd.year_num,
        dd.month_num,
        dr.route_key,
        dr.route_name,
        COUNT(*)                      AS total_flights,
        SUM(ff.tickets_sold)          AS total_tickets_sold,
        SUM(ff.total_ticket_revenue)  AS total_revenue,
        SUM(ff.estimated_fuel_cost)   AS total_est_fuel_cost,
        SUM(ff.total_profit)          AS total_profit,
        ROUND(AVG(ff.load_factor_pct), 2)    AS avg_load_factor_pct,
        ROUND(AVG(ff.arrival_delay_min), 2)  AS avg_arrival_delay_min,
        ROUND(AVG(ff.on_time_indicator)*100,2) AS on_time_pct
    FROM fact_flight_metrics ff
         JOIN dim_date  dd ON ff.date_key  = dd.date_key
         JOIN dim_route dr ON ff.route_key = dr.route_key
    GROUP BY dd.year_num, dd.month_num, dr.route_key, dr.route_name;

    COMMIT;
END;
/
SHOW ERRORS;

-- 4.3 Run population procedure
BEGIN
    pr_populate_agg_monthly_route;
END;
